bool checkPrime (lli n) {
    if (n <= 1)  return false;
    if (n <= 3)  return true;

    if (n % 2 == 0 || n % 3 == 0) return false;

    for (lli i = 5; i * i <= n; i = i + 6)
        if (n % i == 0 || n % (i + 2) == 0)
            return false;

    return true;
}

void solve() {
    lli n; cin >> n;
    if (n & 1 && n > 1) return void(cout << "Ashishgup" << enl);
    else {
        if (n == 1) return void(cout << "FastestFinger" << enl);
        if (!(n & (n - 1)) && n != 2) return void(cout << "FastestFinger" << enl);
        if (checkPrime(n / 2) && n / 2 >= 3) return void(cout << "FastestFinger" << enl);
    }
    cout << "Ashishgup" << enl;
}

